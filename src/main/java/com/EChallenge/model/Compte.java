package com.EChallenge.model;

public class Compte {
	
	private int compteId;
	private String password;
		
	public int getId() {
		return compteId;
	}

	public void setId(int id) {
		this.compteId = id;
	}

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}


}
